from __future__ import annotations
from django.urls import reverse


class Obj:
    def __init__(self, **kwargs: object) -> None:
        self.__dict__.update(kwargs)

    def __bool__(self) -> bool:
        return True


class ServiceObj(Obj):
    def get_absolute_url(self) -> str:
        return reverse("service_detail", kwargs={"slug": self.slug})  # type: ignore[attr-defined]

    def price_range_display(self) -> str:
        pf = getattr(self, "price_from", None)
        pt = getattr(self, "price_to", None)
        if pf and pt:
            return f"{pf}–{pt} грн"
        if pf:
            return f"від {pf} грн"
        return "Уточнювати"


